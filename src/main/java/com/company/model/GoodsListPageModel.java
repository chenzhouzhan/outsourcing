package com.company.model;

/**
 * @author czz
 *
 */
public class GoodsListPageModel {
	private String name;

	private int num;

	private double price;

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            要设置的 name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return num
	 */
	public int getNum() {
		return num;
	}

	/**
	 * @param num
	 *            要设置的 num
	 */
	public void setNum(int num) {
		this.num = num;
	}

	/**
	 * @return price
	 */
	public double getPrice() {
		return price;
	}

	/**
	 * @param price
	 *            要设置的 price
	 */
	public void setPrice(double price) {
		this.price = price;
	}

}
